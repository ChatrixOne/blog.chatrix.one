  /* ── Typing animation helpers ─────────────────────────────── */
  function getInputField()  { return document.getElementById('chatInputField'); }
  function getSendBtn()     { return document.getElementById('chatSendBtn'); }
  function getCursor()      { return document.querySelector('.chat-input-cursor'); }

  /* Show a floating key label above the input */
  function showKeyFlash(char, inputEl) {
    if (!inputEl) return;
    var kf = document.createElement('span');
    kf.className = 'key-flash';
    kf.textContent = char === ' ' ? '⎵' : char;
    inputEl.parentNode.style.position = 'relative';
    inputEl.parentNode.appendChild(kf);
    setTimeout(function() { if (kf.parentNode) kf.parentNode.removeChild(kf); }, 320);
  }

  /* Type text into the input field character by character */
  async function typeIntoInput(text, token) {
    var field = getInputField();
    var cursor = getCursor();
    if (!field || !cursor) return;

    field.classList.add('is-typing');
    cursor.classList.add('active');

    /* Clear to just the cursor */
    while (field.firstChild && field.firstChild !== cursor) {
      field.removeChild(field.firstChild);
    }
    field.insertBefore(document.createTextNode(''), cursor);

    var textNode = field.firstChild;

    for (var i = 0; i < text.length; i++) {
      if (runChatScript._currentToken !== token) return;
      textNode.textContent += text[i];
      showKeyFlash(text[i], field);
      playKeySound();
      /* Human-like variable delay: 60-130ms, slightly longer after spaces */
      var charDelay = text[i] === ' ' ? 110 + Math.random() * 60
                                      : 55  + Math.random() * 75;
      await wait(charDelay);
    }
  }

  /* Clear the input field, remove cursor active state */
  function clearInput() {
    var field = getInputField();
    var cursor = getCursor();
    var sendBtn = getSendBtn();
    if (field) {
      field.classList.remove('is-typing');
      /* Remove text node, keep cursor span */
      while (field.firstChild && field.firstChild !== cursor) {
        field.removeChild(field.firstChild);
      }
    }
    if (cursor) cursor.classList.remove('active');
    if (sendBtn) sendBtn.classList.remove('is-sending');
  }

  /* Animate send button press */
  async function animateSend(token) {
    var btn = getSendBtn();
    if (!btn) return;
    playSendSound();
    btn.classList.add('is-sending');
    await wait(350);
    if (runChatScript._currentToken !== token) return;
    btn.classList.remove('is-sending');
  }

  async function runChatScript() {
    if (chatTimer) { clearTimeout(chatTimer); chatTimer = null; }
    clearInput();

    const body = document.getElementById('chatBody');
    body.innerHTML = '';
    const script = makeChatScripts()[currentLang];

    const tokenAtStart = Symbol('run');
    runChatScript._currentToken = tokenAtStart;

    let delay = 600;
    for (const step of script) {
      await wait(delay);
      if (runChatScript._currentToken !== tokenAtStart) return;

      if (step.typing) {
        /* Incoming: show typing dots */
        const t = buildTyping();
        body.appendChild(t);
        scrollChat(body);
        await wait(step.duration || 1500);
        if (runChatScript._currentToken !== tokenAtStart) return;
        playReceiveSound();
        t.remove();
        delay = 200;

      } else if (step.who === 'me') {
        /* Outgoing: type into input field, then send */
        await typeIntoInput(step.text, tokenAtStart);
        if (runChatScript._currentToken !== tokenAtStart) return;

        /* Pause as if reviewing */
        await wait(320);
        if (runChatScript._currentToken !== tokenAtStart) return;

        /* Flash send button */
        await animateSend(tokenAtStart);
        if (runChatScript._currentToken !== tokenAtStart) return;

        /* Clear input and add message to chat with fly-in */
        clearInput();
        const m = buildMessage(step);
        m.classList.add('fly-in');
        body.appendChild(m);
        scrollChat(body);
        delay = 1100;

      } else {
        /* Incoming message — just appears */
        playReceiveSound();
        const m = buildMessage(step);
        m.classList.add('fly-in');
        body.appendChild(m);
        scrollChat(body);
        delay = 1300;
      }
    }
    /* After chat ends — show call screen, then restart */
    chatTimer = setTimeout(runCallScreen, 1800);
  }

